import { GetServerSideProps } from "next";
import { useEffect, useState } from "react";
import DeleteButton from "../../components/list/DeleteButton";

import PageTitle from "../../components/PageTitle";
import useSidebarContext from "../../context/SidebarContext";
import MediaModel from "../../models/MediaModel";
// import ListModel from "../../models/listModel";
import SavedCard from "../../components/list/SavedCard";
import DeleteMediaModal from "../../components/list/DeleteMediaModal";
import { getMedia } from "../../api/media";
import useRefresh from "../../hooks/useRefresh";
import Head from "next/head";
import { staggerContainer } from "../../animations/StaggerCards";
import { motion } from "framer-motion";
import prisma from "../../lib/prisma";

type Props = { listId: string; list: any; initialMedia: MediaModel[] };

export const getServerSideProps: GetServerSideProps = async (context) => {
   const { listId } = context.query!;
   const listData = await prisma.list.findUnique({
      where: { id: listId as string },
   });
   const list = JSON.parse(JSON.stringify(listData));

   const mediaData = await prisma.media.findMany({
      where: { listId: listId as string },
   });
   const media = JSON.parse(JSON.stringify(mediaData));
   return {
      props: { listId, list, initialMedia: media },
   };
};

export default function ListID({ listId, list, initialMedia }: Props) {
   const { openSidebar } = useSidebarContext();

   const [media, setMedia] = useState<MediaModel[]>(initialMedia);
   const [mediaToDelete, setMediaToDelete] = useState<MediaModel[]>([]);

   const { search, refresh } = useRefresh();
   useEffect(() => {
      const getMediaOnRefresh = async () => {
         const media = await getMedia(listId);
         setMedia(media);
      };
      getMediaOnRefresh();
   }, [listId, search]);

   const [isDeleteOpen, setIsDeleteOpen] = useState<boolean>(false);
   const openDelete = () => {
      setIsDeleteOpen(true);
   };
   const closeDelete = () => {
      setIsDeleteOpen(false);
      setMediaToDelete([]);
   };

   const [isDeleteModalOpen, setIsDeleteModalOpen] = useState<boolean>(false);
   const openDeleteModal = () => {
      setIsDeleteModalOpen(true);
   };
   const closeDeleteModal = () => {
      setIsDeleteModalOpen(false);
      closeDelete();
   };

   return (
      <div>
         <Head>
            <title>{list.name}</title>
            <meta name="description" content="Generated by create next app" />
            <link rel="icon" href="/favicon.ico" />
         </Head>
         <PageTitle>{list.name}</PageTitle>
         <motion.div
            variants={staggerContainer}
            initial="initial"
            animate="animate"
            exit="exit"
            className={`gap-5 grid grid-cols-2 md:grid-cols-3 overflow-hidden ${
               openSidebar
                  ? "lg:grid-cols-3 xl:grid-cols-4 2xl:grid-cols-5"
                  : "lg:grid-cols-4 xl:grid-cols-5 2xl:grid-cols-6"
            }`}
         >
            {media.map((media) => (
               <SavedCard
                  key={media.name}
                  media={media}
                  isDeleteOpen={isDeleteOpen}
                  mediaToDelete={mediaToDelete}
                  setMediaToDelete={setMediaToDelete}
               />
            ))}
         </motion.div>

         <DeleteButton
            mediaToDelete={mediaToDelete}
            isDeleteOpen={isDeleteOpen}
            onClick={isDeleteOpen ? closeDelete : openDelete}
            openDeleteModal={openDeleteModal}
         />
         <DeleteMediaModal
            list={list}
            isOpen={isDeleteModalOpen}
            close={closeDeleteModal}
            mediaToDelete={mediaToDelete}
            refresh={refresh}
         />
      </div>
   );
}
